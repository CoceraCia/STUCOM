    private void SubmitAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitAddButtonActionPerformed
        // TODO add your handling code here:
        FileManager fm = new FileManager();
        String name = jTextFieldName.getText();
        String lastName = jTextFieldLastName.getText();
        int age;
        try{
            age = Integer.parseInt(jTextFieldAge.getText());
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"There's Empty Data", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String course = jTextFieldCourse.getText();
        String dni = jTextFieldDni.getText();
        if (!name.equals("") && !lastName.equals("") && !course.equals("") && !dni.equals("")){
            if (RegistrosAlumnos.repeatedDni(dni)) {
                JOptionPane.showMessageDialog(null,"DNI Already exists", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                Alumno alu = new Alumno(name, lastName, age, course, dni);
                RegistrosAlumnos.getRegister().add(alu);
                String data = name + ";" + lastName + ";" + age + ";" + course + ";" + dni + "\n";
                fm.openFile(RegistrosAlumnos.getFile(), data, true);
                jLabelResult.setForeground(Color.GREEN);
                jLabelResult.setText("STUDENT ADDED");
            }
        } else {
            JOptionPane.showMessageDialog(null,"There's Empty Data", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SubmitAddButtonActionPerformed

    private void jTextFieldAgeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldAgeKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
            if (!Character.isDigit(c)) { // Si no es n√∫mero, cancelar el evento
                evt.consume(); //funcion que cancela el evento
        }
    }//GEN-LAST:event_jTextFieldAgeKeyTyped

    //funcion para comprobar que en el campo del DNI sea correcto
    private void jTextFieldDniKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldDniKeyTyped
        // TODO add your handling code here:
        KeyTypedPatterns key = new KeyTypedPatterns();
        if(!key.patDni(evt, jTextFieldDni.getText(), jTextFieldDni.getCaretPosition())){
            evt.consume();
        }
        
    }//GEN-LAST:event_jTextFieldDniKeyTyped


