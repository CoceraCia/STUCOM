    private void jTextFieldISBNKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldISBNKeyTyped
        // TODO add your handling code here:
         // TODO add your handling code here:
        char c = evt.getKeyChar();
        KeyTypedPatterns pat = new KeyTypedPatterns(evt);
        if (!pat.patternISBN(jTextFieldISBN)) {
            return;
        }
        if (BookInventory.obtainBook(jTextFieldISBN.getText()+c) != null) {
            jLabelResult.setForeground(Color.green);
            jLabelResult.setText("FOUND");
        } else {
            jLabelResult.setForeground(Color.red);
            jLabelResult.setText("NOT FOUND");
        }
    }//GEN-LAST:event_jTextFieldISBNKeyTyped

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        Book b = BookInventory.obtainBook(jTextFieldISBN.getText());
        if(b == null){
            JOptionPane.showMessageDialog(null, "Libro no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            if (authors.size() == 0 || jTextFieldISBN.getText().equals("") || jTextFieldTitle.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Valores vacios", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            b.setTitle(jTextFieldTitle.getText());
            b.setAuthors(authors);
            b.setPrice(Double.parseDouble(jTextFieldPrice.getText()));
            b.setStock( Integer.parseInt(jTextFieldStock.getText()));
            BookInventory.writeBooks();
            jEditorPane1.setText(BookInventory.printBooks());
            JOptionPane.showMessageDialog(null, "Insertado correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Valores vacios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

