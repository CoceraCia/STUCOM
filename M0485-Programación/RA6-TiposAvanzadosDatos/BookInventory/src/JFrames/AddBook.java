    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        try{
            if (authors.size() == 0 || jTextFieldISBN.getText().equals("") || jTextFieldTitle.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Valores vacios", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            BookInventory.books.put(jTextFieldISBN.getText(), new Book(
                jTextFieldISBN.getText(),
                jTextFieldTitle.getText(), authors,
                Double.parseDouble(jTextFieldPrice.getText()),
                Integer.parseInt(jTextFieldStock.getText())));
                BookInventory.writeBooks();
                JOptionPane.showMessageDialog(null, "Insertado correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Valores vacios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
    }//GEN-LAST:event_jButtonAddActionPerformed

