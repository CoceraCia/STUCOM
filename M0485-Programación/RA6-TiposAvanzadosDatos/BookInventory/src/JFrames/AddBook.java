    private void printJPane() {
        String text = "Title: " + jTextFieldTitle.getText() + "\n"
                + "ISBN: " + jTextFieldISBN.getText() + "\n"
                + "Price: " + jTextFieldPrice.getText() + "\n"
                + "Stock: " + jTextFieldStock.getText() + "\n"
                + "Authors: " + "\n";
        for (int i = 0; i < authors.size(); i++) {
            text += "  -  " + authors.get(i) + "\n";

        }
        text += "  -  " + jTextFieldAuthor.getText() + "\n";
        jEditorPane1.setText(text);

    }

